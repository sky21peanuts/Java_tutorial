class ConditionSample{
    public static void main(String[] args){
        int a = 0, b = -2;

        System.out.println("1 < 2 is " + (1 < 2));
        System.out.println("4 <= a is " + (4 <= a));
        System.out.println("b < a is " + (b < a));
        System.out.println("0 == a is " + (0 == a));
        System.out.println("b != -2 is "+ (b != -2));
    }
}
